# Create a program that reads two numbers and displays the sum between them.

num1 = float(input("Enter the first value: "))  # Read the first number
num2 = float(input("Enter the second value: "))  # Read the second number
sum = num1 + num2
print(f"The sum of {num1} and {num2} is {sum:.4}!!")  # Prints the result of the sum, limiting the decimal places to 3
