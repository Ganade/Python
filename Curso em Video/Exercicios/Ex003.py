# Crie um programa que leia dois números e mostre a soma entre eles.

num1 = float(input("Digite o primeiro valor: "))  # Le o primeiro valor
num2 = float(input("Digite o segundo valor: "))  # Le o segundo valor
soma = num1 + num2
print(f"A soma de {num1} e {num2} é {soma:.4}!!")  # Imprime o resultado da soma, limitando as casas decimais a 3
